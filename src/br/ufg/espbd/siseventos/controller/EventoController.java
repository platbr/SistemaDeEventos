    public void addTable() {
        DefaultTableModel dtm = (DefaultTableModel) TabelaEvento.getModel();
        dtm.setNumRows(0);
        for (Evento c : new EventoDAOImpl().listar()) {
            dtm.addRow(new Object[]{c.getId(), c.getNome(), c.getInicio(), c.getFim(), c.getLocal()});

        }

    }
